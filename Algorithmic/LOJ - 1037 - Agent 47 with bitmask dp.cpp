int Set(int N,int pos)
{
    return N=N | (1<<pos);
}
int reset(int N,int pos)
{
    return N= N & ~(1<<pos);
}
bool check(int N,int pos)
{
    return (bool)(N & (1<<pos));
}

int health[16],n;
char w[16][16];
ll dp[1<<16+2];
ll FuN(int mask)
{
    if(mask == (1<<n)-1)
        return 0;
    if(dp[mask] != -1)
        return dp[mask];
    ll mn=longlimit;
    for(int i=0 ; i<n ; i++)
    {
        if(check(mask,i) == 0)  /** If ith enemy is not killed yet **/
        {
            ll ret,tot_shot;
            ret = health[i];    /** Initially ret=health[i] ; bcoz if we haven't killed any enemy yet which weapon can
            be used in ith enemy ; then,we've to killed ith enemy with default weapon which take one shoot for one unit
            health that means total shoot need to kill ith enemy is equal to health[i] **/
            for(int j=0 ; j<n ; j++)
            {
                /** In problem, The jth digit of the ith line denotes the damage done to
                 target j, if you use the weapon of target i in each shot **/
                int digit = w[j][i]-'0';    /** Damage done to target i per shoot,here we use the weapon of
                target j **/
                if(digit!=0 && i!=j && check(mask,j)==1)    /** We can use the weapon of target j only when it already killed **/
                {
                    tot_shot = (health[i]+digit-1)/digit;   /** Calculate Total needed shoot for killing target i by the weapon of target j **/
                    ret = min(ret,tot_shot);    /** Minimum needed shot for killing target i **/
                }
            }
            ret = ret+FuN(Set(mask,i));
            mn=min(mn,ret);
        }
    }
    return dp[mask]=mn;
}
